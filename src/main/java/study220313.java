public class study220313 {
    public static void main(String[] args){
        int a = 1;
        System.out.println(++a); // 전치 : 대입이 되기 전 변수 값이 증가 export=2
        System.out.println(a++); // 후치 : 대입이 된 후 변수 값이 증가   export=2
        System.out.println(a);                                    //export=3

    }
}
